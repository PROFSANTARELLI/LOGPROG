#criar logica para dizer se é de maior ou de menor
idade = int(input("Digite a sua idade: ")) #Variável do tipo inteiro para receber idade

if idade >= 18: #Condicional para saber se é de maior de idade
    print("Você é de maior") #Mostra em tela
else: #condicional para saber se é menor de idade
    print("você é de menor") #Mostra em tela
